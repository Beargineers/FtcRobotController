    }

    private fun doTest(testData: List<Double>, expected: Int) {
        var artifacts = 0
        val ballCounter = BallCounter(13.0, 17.0) {
            artifacts++
        }

        for (distance in testData) {
            ballCounter.update(distance)
        }

        assertEquals(expected, artifacts)
    }
}


